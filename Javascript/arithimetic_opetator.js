// 1 .Arithmetic Operators (For basic calculation)

let a = 10 ;
let b = 3 ;

// addition
let sum = a+b
console.log('addition', sum);

// subtraction

let difference = a - b 
console.log('subtraction', difference);

// Multiplication

let multi = a * b
console.log('multiplication',multi);

// Division

let divided = a / b
console.log(divided.toFixed(2));

// Modulus (Reminder)

let reminder = a % b
console.log('reminder',reminder);